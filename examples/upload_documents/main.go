package main

import (
	"fmt"
	"log"
	"os"

	"encoding/base64"

	"github.com/ardanlabs/conf/v3"
	"github.com/joho/godotenv"

	sila "github.com/kappapay/silaclient"
)

type SilaConfig struct {
	PrivateKeyHex     string `conf:"env:SILA_PRIVATE_KEY_HEX,required"`
	AuthHandle        string `conf:"env:SILA_AUTH_HANDLE,required"`
	BaseURL           string `conf:"env:SILA_BASE_URL,required"`
	UserHandle        string `conf:"env:USER_HANDLE,required"`
	UserPrivateKeyHex string `conf:"USER_PRIVATE_KEY_HEX,required"`
}

func main() {
	err := run()
	if err != nil {
		log.Fatal(err)
	}
}

func run() error {
	if _, err := os.Stat(".env"); err == nil {
		err := godotenv.Load()
		if err != nil {
			return fmt.Errorf("error loading .env file: %w", err)
		}
	}

	cfg := SilaConfig{}
	_, err := conf.Parse("", &cfg)
	if err != nil {
		return fmt.Errorf("parsing config: %w", err)
	}
	client, err := sila.NewClient(cfg.PrivateKeyHex, cfg.AuthHandle, sila.Environment(cfg.BaseURL))
	if err != nil {
		return fmt.Errorf("error initializing client: %w", err)
	}

	userHandle := cfg.UserHandle
	userPrivateKey := cfg.UserPrivateKeyHex

	fmt.Println("user_handle:", userHandle)
	fmt.Println("private_key:", userPrivateKey)

	fileContents, err := base64.StdEncoding.DecodeString(file)
	if err != nil {
		return err
	}

	resp, err := client.Documents(userHandle).
		AddFile(sila.FileMetadata{
			Name:         "a",
			Filename:     "a.png",
			MimeType:     "image/png",
			DocumentType: "tax_w2",
			Description:  "tax_w2",
		}, fileContents).
		Do(userPrivateKey)
	if err != nil {
		return err
	}

	if !resp.Success {
		return fmt.Errorf("sila call failed: %v", resp)
	}

	fmt.Println("resp:", resp)
	return nil

}

